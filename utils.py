def add(a: int, b: int) -> int:
    """FUNKCJA DODAWANIA"""
    return a + b


def subtract(a: int, b: int) -> int:
    """FUNKCJA ODEJMOWANIA"""
    return a - b


def multiply(a: int, b: int) -> int:
    """FUNKCJA MNOZENIA"""
    return a * b


def divide(a: int, b: int) -> float:
    """FUNKCJA DZIELENIA"""
    return a / b
